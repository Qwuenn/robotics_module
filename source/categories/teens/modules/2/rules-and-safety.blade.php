    <p>
        The Civil Aviation Authority of the Philippines released a resolution last April 2011 revising the Civil Aviation
        Regulation regulating the use of drones in the Philippines. Flying of drones is still legal in the Philippines provided
        that the operator must adhere to the following:
    </p>

    <ul>
        <li>
            No flying over the populous area (Flying over a gathering of people in an event).
        </li>
        <li>
            Stay clear of the populated area (Flying over housing subdivision).
        </li>
        <li>
            No flying at 400 feet and above.
        </li>
        <li>
            No flying within a 10 km radius of any airport.
        </li>
        <li>
            No flying over-controlled or prohibited airspace. (Aircraft approach and take-off route, military camps, the
            presidential palace).
        </li>
        <li>
            No commercial drone operation without an RPA controller Certificate.
        </li>
        <li>
            Flying drones weighing more than 7 kgs require an RPA controller certificate and it requires drone registration at CAAP.
        </li>
        <li>
            Drones with a gross weight of 150kgs and above are required to obtain a Special Certificate of Airworthiness (SCA) or an
            Experimental Certificate (EC).
        </li>
        <li>
            RPA Controller doing non-commercial operations may operate only within Visual Line of Sight.
        </li>
        <li>
            No night flying.
        </li>
        <li>
            Drone flying display or air show requires a permit from CAAP.
        </li>
    </ul>
@endsection